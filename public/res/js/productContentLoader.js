const product = new URLSearchParams(window.location.search).get("q");

if (products[product]) {
  // load main image and main title
  $("#js-product-name").html(`${products[product].productName}`);
  $("#js-main-img").attr("src", `${products[product].image}`);

  // normally different images of the same product
  const selectorImgs = $(".img-selector").children("img");
  for (let i = 0; i < selectorImgs.length; i++) {
    $(selectorImgs[i]).attr("src", `${products[product].image}`);
  }

  // gets information
  var informationContent = products[product].content.split(";");

  let lines;
  // loads information
  for (lines = 0; lines < informationContent.length && lines < 6; lines++) {
    $("#js-information-list").append(
      `<li>
        ${informationContent[lines]}
      </li>`
    );
  }
  // add padding depending on lines
  $(".product-bottom-infos").css({ "margin-top": `${(7 - lines) * 28}px` });
  $("#js-product-price").html(`${products[product].price}€`);

  // ********* random recommendet products ************

  // generate random numbers
  var randNums = [];
  while (randNums.length < 4) {
    var r = Math.floor(Math.random() * 24);
    if (randNums.indexOf(r) === -1) randNums.push(r);
  }

  // selecting images and names
  const recommendedProductNames = $(".js-recommended-product-name");
  const recommendedProductImg = $(".top__img").children("img");
  const linksToProducts = $(".js-link-to-product");
  // editing images, names and links
  let index;
  for (index = 0; index < randNums.length; index++) {
    $(recommendedProductNames[index]).html(
      products.key(randNums[index]).productName
    );
    $(recommendedProductImg[index]).attr(
      "src",
      products.key(randNums[index]).image
    );
    $(linksToProducts[index*2]).attr("href", products.key(randNums[index]).link);
    $(linksToProducts[index*2+1]).attr("href", products.key(randNums[index]).link);

  }
} else {
  //redirect if invalid query string
  window.location = window.location.href.replace(
    "/pages/product/product.html",
    "/index.html"
  );
}
